
Customer Responsibility: Federal customers are responsible for implementing their own 
system use notification banner for their users. New Relic federal customers must implement SSO functionality 
in order to comply with FedRAMP requirements. As a part of this SSO integration, federal customers must ensure 
that their information system displays a system use notification banner that aligns with AC-8 prior to granting 
access to their authorized users. For more information on federal customer user authentication, refer to IA-8

