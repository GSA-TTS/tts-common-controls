
Customer Responsibility: New Relic One Platform is classified as a FIPS-199 moderate risk impact system, 
so classified data is not stored, processed, or transmitted within the system. It is the responsibility of federal customers 
to ensure that unauthorized information is not stored or transmitted within the New Relic One Platform.

Controls Reference: IR-09, IR-09(01), IR-09(02), IR-09(03), IR-09(04)

Status: Implemented

Based on the FIPS-199 assessment of government information, FOUO, CUI, PII, sensitive, or classified information is 
neither transmitted, collected, or stored in New Relic. Information shared with the New Relic platform is limited to 
application errors and performance, Public IPs and DNS records, and general notification and alerts based on that data. 



