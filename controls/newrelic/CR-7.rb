
Customer Responsibility: Federal customers are responsible to install the New Relic agents based on their 
applications coding language. Various agents include PHP, C, Go, Java, .Net, Python, Node.js, and Ruby. 
The agents implement cryptographic mechanisms to prevent the unauthorized disclosure of information during transmission 
through library dependencies and code development. 
Customers are responsible for certification and accreditation of these components as part of the on-premise 
FISMA authorization efforts for continuous monitoring which includes activities such as updating and patching of 
these components in coordination with New Relic.  Customers are responsible to meet the applicable System and 
Information Integrity (SI) controls associated with these components.

Controls Reference: SC-08(01)

Status: Implemented (Customer Configured)

New Relic agents are installed and configured by system owners and assessed as part of their system ATO. 



